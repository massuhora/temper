任何观点都由三个基本构件组成：

1. **论题（Issue）**：引起对话或讨论的问题，是描述性论题（是什么）还是规定性论题（应不应该）。
2. **结论（Conclusion）**：作者希望你接受的信息，通常是带有价值判断的断言。
3. **理由（Reasons）**：支撑结论的论据，包括事实、数据、案例、专家意见等。

